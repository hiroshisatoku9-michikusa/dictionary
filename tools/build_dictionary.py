def clean_misreading_ja(text: str) -> str:
    text = text.strip()
    text = re.sub(r"。$", "", text)
    text = re.sub(r"^.+?ではなく、", "", text)
    text = re.sub(r"^.+?ではなく", "", text)
    return text.strip(" 、。")


def category_for(term: str) -> dict:
    for ja, (en, terms) in CATEGORIES.items():
        if term in terms:
            return {"ja": ja, "en": en}
    return {"ja": "その他", "en": "Other"}


def sources_for(term: str) -> list[dict]:
    sources = []
    for article_id, terms in SOURCE_GROUPS.items():
        if term in terms:
            sources.append(ARTICLES[article_id])
    if not sources:
        sources.append(ARTICLES["map"])
    return sources


def parse_source() -> list[dict]:
    text = SOURCE.read_text(encoding="utf-8")
    entries = []
    row_re = re.compile(r"^\|\s*(\d+)\s*\|\s*([^|]+?)\s*\|\s*([^|]+?)\s*\|$")
    for line in text.splitlines():
        match = row_re.match(line)
        if not match:
            continue
        rank = int(match.group(1))
        term = match.group(2).strip()
        misreading_ja = clean_misreading_ja(match.group(3).strip())
        slug = re.sub(r"[^0-9A-Za-zぁ-んァ-ヶ一-龠ー]+", "-", term).strip("-")
        entries.append(
            {
                "id": f"{rank:03d}-{slug}",
                "rank": rank,
                "term": {"ja": term, "en": TERM_EN[term]},
                "kana": "",
                "category": category_for(term),
                "standardMeaning": {
                    "ja": STANDARD_JA[term],
                    "en": STANDARD_EN[term],
                },
                "misreading": {
                    "ja": misreading_ja,
                    "en": MISREADING_EN[term],
                },
                "sources": sources_for(term),
                "notes": "",
                "status": "seed",
            }
        )
    return entries


def main() -> None:
    entries = parse_source()
    if MANUAL.exists():
        manual_entries = json.loads(MANUAL.read_text(encoding="utf-8"))
        entries.extend(manual_entries)
    JSON_OUT.write_text(json.dumps(entries, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    JS_OUT.write_text(
        "window.MISREADING_DICTIONARY = "
        + json.dumps(entries, ensure_ascii=False, indent=2)
        + ";\n",
        encoding="utf-8",
    )
    print(f"Wrote {len(entries)} entries")
    print(JSON_OUT)
    print(JS_OUT)


if __name__ == "__main__":
    main()
